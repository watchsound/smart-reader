/**
 * Generate a unique ID for learning entities
 */
export function generateLearningPointId(): string {
  const timestamp = Date.now().toString(36);
  const randomPart = Math.random().toString(36).substring(2, 8);
  return `lp_${timestamp}_${randomPart}`;
}

/**
 * Generate a unique ID for book chunks
 */
export function generateChunkId(): string {
  const timestamp = Date.now().toString(36);
  const randomPart = Math.random().toString(36).substring(2, 8);
  return `chunk_${timestamp}_${randomPart}`;
}

/**
 * Generate a unique ID for concepts
 */
export function generateConceptId(): string {
  const timestamp = Date.now().toString(36);
  const randomPart = Math.random().toString(36).substring(2, 8);
  return `concept_${timestamp}_${randomPart}`;
}

/**
 * Create a simple text card from vocabulary
 */
export function createVocabularyCard(
  word: string,
  definition: string,
  extras?: Partial<LearningPointExtras>
): Omit<UniversalLearningPoint, 'id' | 'userId' | 'createdAt'> {
  return {
    itemType: 'word',
    domainType: DomainType.VOCABULARY,
    title: word,
    front: { text: word },
    back: { text: definition },
    extras: extras as LearningPointExtras,
    difficulty: DifficultyLevel.INTERMEDIATE,
    estimatedTimeMinutes: 1,
    status: 'pending',
    masteryLevel: 0,
    reviewCount: 0,
    correctStreak: 0,
  };
}

/**
 * Create a concept card
 */
export function createConceptCard(
  name: string,
  explanation: string,
  keyPoints?: string[]
): Omit<UniversalLearningPoint, 'id' | 'userId' | 'createdAt'> {
  return {
    itemType: 'concept',
    domainType: DomainType.KNOWLEDGE,
    title: name,
    front: { text: `What is ${name}?` },
    back: { text: explanation },
    extras: keyPoints ? { keyPoints } : undefined,
    difficulty: DifficultyLevel.INTERMEDIATE,
    estimatedTimeMinutes: 3,
    status: 'pending',
    masteryLevel: 0,
    reviewCount: 0,
    correctStreak: 0,
  };
}

/**
 * Create a formula card
 */
export function createFormulaCard(
  name: string,
  formula: string,
  explanation: string,
  variables?: { symbol: string; meaning: string }[]
): Omit<UniversalLearningPoint, 'id' | 'userId' | 'createdAt'> {
  return {
    itemType: 'formula',
    domainType: DomainType.MATH,
    title: name,
    front: { text: `What is the formula for ${name}?` },
    back: { text: explanation, latex: formula },
    extras: variables ? { variables } : undefined,
    difficulty: DifficultyLevel.INTERMEDIATE,
    estimatedTimeMinutes: 2,
    status: 'pending',
    masteryLevel: 0,
    reviewCount: 0,
    correctStreak: 0,
  };
}

/**
 * Calculate mastery level change after review
 */
export function calculateMasteryChange(
  currentMastery: number,
  wasCorrect: boolean,
  correctStreak: number
): number {
  if (wasCorrect) {
    // Increase mastery, with bonus for streaks
    const increase = 10 + Math.min(correctStreak * 2, 10);
    return Math.min(100, currentMastery + increase);
  } else {
    // Decrease mastery on incorrect
    const decrease = 15;
    return Math.max(0, currentMastery - decrease);
  }
}

/**
 * Determine status from mastery level
 */
export function getStatusFromMastery(
  masteryLevel: number,
  reviewCount: number
): LearningPointStatus {
  if (masteryLevel >= 90 && reviewCount >= 5) {
    return 'mastered';
  } else if (reviewCount > 0) {
    return 'reviewing';
  } else if (masteryLevel > 0) {
    return 'learning';
  }
  return 'pending';
}

