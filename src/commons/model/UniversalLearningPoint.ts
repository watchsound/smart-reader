/**
 * UniversalLearningPoint.ts
 *
 * A universal abstraction for any learnable content represented as cards.
 * This model bridges the gap between different content types (vocabulary, concepts,
 * formulas, facts) and the spaced repetition system.
 *
 * Design Philosophy:
 * - Any learning goal decomposes into atomic "learning points"
 * - Each learning point is represented as a card (front/back)
 * - Cards can be linked to source chunks and concepts
 * - Supports both manual creation and AI generation
 */

import { DomainType, DifficultyLevel, ContentType } from './LearningDomains';

// =============================================================================
// LEARNING POINT TYPES
// =============================================================================

/**
 * Type of learning point - determines card structure and study behavior
 */
export type LearningPointType =
  | 'word' // Vocabulary word
  | 'concept' // Abstract concept/idea
  | 'formula' // Mathematical formula
  | 'rule' // Grammar/logic rule
  | 'fact' // Historical fact, date, etc.
  | 'technique' // Skill or procedure
  | 'example' // Worked example
  | 'problem' // Practice problem
  | 'definition' // Term definition
  | 'relationship' // Connection between concepts
  | 'quote' // Notable quote
  | 'code' // Code snippet
  | 'custom'; // User-defined type

/**
 * Source type - where did this learning point come from?
 */
export type LearningPointSourceType =
  | 'book_chunk' // Extracted from book chunk
  | 'vocabulary' // From vocabulary table
  | 'note' // From user note
  | 'quiz' // From quiz problem
  | 'highlight' // From book highlight
  | 'url' // From web content
  | 'manual' // Manually created by user
  | 'ai_generated'; // Generated by AI

/**
 * Status of a learning point in the learning plan
 */
export type LearningPointStatus =
  | 'pending' // Not yet started
  | 'learning' // Currently learning (new)
  | 'reviewing' // In review cycle
  | 'mastered'; // Fully learned

// =============================================================================
// CARD CONTENT
// =============================================================================

/**
 * Content for one side of a learning card
 * Supports multiple content types for rich learning experiences
 */
export interface CardContent {
  /** Main text content (supports markdown) */
  text: string;

  /** Optional rich HTML content */
  html?: string;

  /** Optional image (URL or base64) */
  image?: string;

  /** Optional audio URL (for pronunciation) */
  audio?: string;

  /** Optional LaTeX formula */
  latex?: string;

  /** Optional code snippet */
  code?: {
    language: string;
    content: string;
  };
}

/**
 * Extended content for specific learning point types
 */
export interface LearningPointExtras {
  // For vocabulary
  pronunciation?: string;
  partOfSpeech?: string;
  etymology?: string;
  synonyms?: string[];
  antonyms?: string[];
  exampleSentences?: string[];

  // For formulas
  variables?: { symbol: string; meaning: string }[];
  derivation?: string;
  applications?: string[];

  // For concepts
  keyPoints?: string[];
  commonMisconceptions?: string[];
  relatedConcepts?: string[];

  // For problems
  solution?: string;
  hints?: string[];
  difficulty?: number; // 1-10

  // For code
  explanation?: string;
  output?: string;
  timeComplexity?: string;
  spaceComplexity?: string;

  // Generic
  tags?: string[];
  notes?: string;
}

// =============================================================================
// UNIVERSAL LEARNING POINT
// =============================================================================

/**
 * UniversalLearningPoint - The core abstraction for any learnable content
 *
 * This interface unifies different content types into a single card-based
 * format that works with spaced repetition systems.
 */
export interface UniversalLearningPoint {
  // === Identification ===

  /** Unique identifier (UUID) */
  id: string;

  /** User who owns this learning point */
  userId: number;

  /** Type of learning point */
  itemType: LearningPointType;

  /** Domain this belongs to */
  domainType: DomainType;

  // === Card Content ===

  /** Title/name of the learning point */
  title: string;

  /** Front of card (question/prompt) */
  front: CardContent;

  /** Back of card (answer/explanation) */
  back: CardContent;

  /** Domain-specific extra fields */
  extras?: LearningPointExtras;

  // === Metadata ===

  /** Difficulty level */
  difficulty: DifficultyLevel;

  /** Estimated time to learn (minutes) */
  estimatedTimeMinutes: number;

  /** User-defined tags */
  tags?: string[];

  // === Learning Mechanics ===

  /** IDs of prerequisite learning points */
  prerequisites?: string[];

  /** IDs of related learning points */
  relatedItems?: string[];

  /** Content types this point teaches */
  contentTypes?: ContentType[];

  // === Status (managed by learning plan) ===

  /** Current learning status */
  status: LearningPointStatus;

  /** Mastery level 0-100 */
  masteryLevel: number;

  /** Day in plan when this should be introduced */
  scheduledDay?: number;

  /** Phase in plan this belongs to */
  phase?: number;

  // === Spaced Repetition ===

  /** Link to sr_item table for FSRS tracking */
  srItemId?: string;

  /** Last review timestamp */
  lastReviewedAt?: string;

  /** Next scheduled review */
  nextReviewAt?: string;

  /** Number of times reviewed */
  reviewCount: number;

  /** Consecutive correct answers */
  correctStreak: number;

  // === Source Reference ===

  /** Where this learning point came from */
  sourceType?: LearningPointSourceType;

  /** ID in source table (e.g., vocabulary.id, note_json.id) */
  sourceId?: string;

  /** Additional source context (page number, chapter, URL path) */
  sourceContext?: string;

  /** ID of the chunk this was extracted from (if from book) */
  chunkId?: string;

  // === AI Generation ===

  /** AI provider used for generation */
  generatedBy?: string;

  /** Prompt used (for regeneration) */
  generationPrompt?: string;

  /** AI confidence score 0-1 */
  confidence?: number;

  // === Timestamps ===

  createdAt: string;
  updatedAt?: string;
}

