/**
 * LearningTopic.ts
 *
 * Data models for the AI Learning Companion Framework.
 * Defines interfaces for Learning Topics, Plans, Sessions, and Performance tracking.
 *
 * Core Entities:
 * - LearningTopic: User's learning goal (e.g., "GRE Vocabulary", "Python Basics")
 * - LearningPlan: AI-generated curriculum with phases and daily goals
 * - LearningSession: Single study session with performance data
 * - LearningItemPerformance: Per-item performance tracking
 */

import {
  DomainType,
  TopicStatus,
  SessionType,
  DifficultyLevel,
  TopicSourceType,
  ContentType,
  AssessmentMethod,
} from './LearningDomains';

// =============================================================================
// LEARNING TOPIC - User's learning goal
// =============================================================================

/**
 * A Learning Topic represents a user's learning goal.
 * It's an instance of a domain type with specific content and progress tracking.
 *
 * Examples:
 * - "GRE Vocabulary" (domain: vocabulary)
 * - "Python Programming" (domain: skill)
 * - "The Art of War - Book Study" (domain: knowledge)
 * - "Japanese Language" (domain: language)
 * - "Calculus I" (domain: math)
 */
export interface LearningTopic {
  id: string;
  userId: number;

  // Core identification
  name: string;
  description: string | null;
  domainType: DomainType;

  // Source linkage (optional - topic can be linked to existing content)
  sourceType: TopicSourceType | null;
  sourceId: string | null; // Book ID, vocabulary set ID, URL, etc.

  // Learning configuration
  targetDate: Date | null; // Target completion date
  dailyTimeMinutes: number; // Daily study time goal
  difficulty: DifficultyLevel | 'auto'; // Starting difficulty or auto-detect

  // Status and progress
  status: TopicStatus;
  progressPercent: number; // 0-100
  masteredItems: number;
  totalItems: number;

  // Engagement tracking
  streakDays: number;
  lastStudiedAt: Date | null;

  // Timestamps
  createdAt: Date;
  updatedAt: Date | null;
}

/**
 * Input for creating a new learning topic
 */
export interface CreateLearningTopicInput {
  name: string;
  description?: string;
  domainType: DomainType;
  sourceType?: TopicSourceType;
  sourceId?: string;
  targetDate?: Date;
  dailyTimeMinutes?: number;
  difficulty?: DifficultyLevel | 'auto';
}

/**
 * Input for updating a learning topic
 */
export interface UpdateLearningTopicInput {
  name?: string;
  description?: string;
  targetDate?: Date | null;
  dailyTimeMinutes?: number;
  difficulty?: DifficultyLevel | 'auto';
  status?: TopicStatus;
}

