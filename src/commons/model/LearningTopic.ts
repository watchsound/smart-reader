/**
 * Check if a topic is active (can be studied)
 */
export function isTopicActive(topic: LearningTopic): boolean {
  return topic.status === TopicStatus.ACTIVE;
}

/**
 * Check if a topic has an active plan
 */
export function topicHasPlan(
  topic: LearningTopic,
  plan: LearningPlan | null
): plan is LearningPlan {
  return plan !== null && plan.status === 'active';
}

/**
 * Calculate days until target date
 */
export function daysUntilTarget(topic: LearningTopic): number | null {
  if (!topic.targetDate) return null;
  const now = new Date();
  const target = new Date(topic.targetDate);
  const diffTime = target.getTime() - now.getTime();
  return Math.ceil(diffTime / (1000 * 60 * 60 * 24));
}

/**
 * Calculate accuracy percentage from session data
 */
export function calculateSessionAccuracy(session: LearningSession): number {
  if (session.itemsReviewed === 0) return 0;
  return (session.itemsCorrect / session.itemsReviewed) * 100;
}

/**
 * Get next review date based on spaced repetition
 * Using a simplified SM-2 like algorithm
 */
export function calculateNextReview(
  correctStreak: number,
  wasCorrect: boolean
): Date {
  const now = new Date();
  let intervalDays: number;

  if (!wasCorrect) {
    // Reset to 1 day on incorrect
    intervalDays = 1;
  } else {
    // Exponential growth based on streak
    switch (correctStreak) {
      case 0:
        intervalDays = 1;
        break;
      case 1:
        intervalDays = 1;
        break;
      case 2:
        intervalDays = 3;
        break;
      case 3:
        intervalDays = 7;
        break;
      case 4:
        intervalDays = 14;
        break;
      case 5:
        intervalDays = 30;
        break;
      default:
        intervalDays = Math.min(90, 30 * Math.pow(1.5, correctStreak - 5));
    }
  }

  return new Date(now.getTime() + intervalDays * 24 * 60 * 60 * 1000);
}

/**
 * Generate a unique ID for learning entities
 */
export function generateLearningId(prefix: string = 'learn'): string {
  const timestamp = Date.now().toString(36);
  const randomPart = Math.random().toString(36).substring(2, 8);
  return `${prefix}_${timestamp}_${randomPart}`;
}
